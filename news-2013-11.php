<h1>News</h1>
<h2>27th November, 2013 - G-Cloud Spend for October 2013</h2>
<p>Spend for October 2013 has now been included.</p><p>Highlights are:</p> 
<ul>
<li>Spend in October was £7.0m.</li>
<li>Spend in September was revised upwards from £5.6m to £8.5m.</li>
<li>There is a trend of upward revision of previous months data - expect Oct to be revised upwards to around £9m in Dec.</li>
<li>Total spend is £63.5m with £56.3m being in 2013 (£7.1m in 2012).</li>
</ul>
<p>Spread of the framework</p>
<ul>
<li>20 new customers joined the framework this month (bringing the total to 306) and .</li> 
<li>15 new suppliers joined the framework this month (bringing the total to 204) and .</li> 
</ul>
<p>Forecasts</p>
<ul>
<li>the first £10m month is likely this year, though we may not know until end Jan 14 given the way that data is being revised for previous periods.</li>
<li>Forecast spend for 2013 should slightly exceed £75m</li>
<li>Forecast spend for 2014 of between £100m and £150m is reasonable.  £200m would not be a surprise.</li>
</ul>
<p>Spend by Lot</p>
<ul>
<li>IaaS increased from £1.9m to £2.6m (26%) - good to see the pace picking up.</li>
<li>PaaS increased from £456k to £579k (21%) - again good to see the pace picking up.</li>
<li>SaaS increased from £11.2m to £12.1m (7%) </li>
<li>SCS increased from £39.8m to £48.1m (8%) - expect growth here to slow as development work migrates to the Digital Services Framework</li>
</ul>

