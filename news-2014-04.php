<p>Spread of the framework</p>
<ul>
<li>A big leap in the number of customers - up 43 this month (bringing the total to 473).</li> 
<li>12 new suppliers joined the framework this month (bringing the total to 312).</li> 
</ul>
<p>Forecasts</p>
<ul>
<li>Spend is building strongly.  The framework is adding 20 to 30 new buying organisations each month (on average).</li>
<li>It is likely that that spend will regularly top £20m a month this quarter and be around £30m a month by year end.</li>
<li>We are holding our forecast for 2014 to £200m but if growth continues, will be revising this upwards in coming months.  £250m is looking achievable and £300m is within sight.  </li>
</ul>

