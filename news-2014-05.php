<h1>G-Cloud News</h1>

<h2>13th June 2014 - G-Cloud Spend to end of May 2014</h2>
<p>Spend for May 2014 has now been included.</p><p>Highlights are:</p> 
<ul>
<li>Total spend is £191m with £97m so far in 2014  (£87m being in 2013, £7.1m in 2012).</li><br>
<li>Spend with Small and Medium Sized Enterprises (SMEs) continues at pace.  We expect spend with SMEs for 2014 to be over £130m, compared to £42m in 2013.  SME spend in 2014 is £61m.</li> <br>
<li>Spend in May was £12m. This is below trend; we think that the reason is mostly a timing issue.  
Last month, spend was reported at £16m and then revised upwards this month to £20m.  
Cabinet Office has started to release data earlier in the month and we think some suppliers struggle to make the earlier reporting deadline.</li><br>
<li>The new Digital Services Framework (DSF) is impacting Lot 4 spend - we are starting to see agile development services move to DSF from g-cloud.  
This is expected and is part of Government's strategy.  'Agile' spend has been comfortably over £1m a month since October 2013.  Last month (April) it was £913k and this month (May) £565k.</li><br>
</ul>

