<h1>G-Cloud News</h1>

<h2>7th Feb 2013 - G-Cloud Spend to end of December 2013</h2>
<p>Spend for December 2013 has now been included.</p><p>Highlights are:</p> 
<ul>
<li>Impressive growth - G-Cloud spend per month (£14m) is now about twice that of the entire year in 2012 (£7m)</li>
<li>Spend in December was £11.1m.  Expect this to be revised upwards when the Jan 2014 data is released (our guess to about £14m).</li>
<li>Spend in November was revised upwards from £10.5m to £14.0m.</li>
<li>There were many significant revisions of spend data going back to the beginning of the framework - details below.</li>
<li>Total spend is £92.6m with £85.6m being in 2013 (£7.1m in 2012).</li>
</ul>
<p>Spread of the framework</p>
<ul>
<li>22 new customers joined the framework this month (bringing the total to 346).</li> 
<li>22 new suppliers joined the framework this month (bringing the total to 238).</li> 
</ul>
<p>Forecasts</p>
<ul>
<li>Total Spend is significantly exceeding our forecasts.  Our original forecast was £75m for 2013, revised last month to £80m.  It looks like
Total Spend will get close to £90m in 2013 when December figures are finalised.</li>
<li>We have increased the forecast spend for 2013 from £80m to £90m</li>
<li>We are holding our forecast spend for 2014 at £150m.  However, based on the revised November figures this month - this is likely top be revised upwards.  
  Based on growth rates in both spend and the number
 of organisations using the framework, £200m+ would not be a surprise in 2014 and £250m is definitely in sight possible.</li>
</ul>
