<h1>G-Cloud News</h1>

<h2>16th April 2014 - G-Cloud Spend to end of March 2014</h2>
<p>Spend for March 2014 has now been included.</p><p>Highlights are:</p> 
<ul>
<li>Very impressive growth - G-Cloud spend per month (£26m in March 2014) is now more than three times that of the entire year in 2012 (£7m)</li>
<li>Spend in March was £26m.  March spend usually spikes - a trend figure would be about £19m.</li>
<li>Total spend is £154m with almost £60m in 2014  (£87m being in 2013, £7.1m in 2012).</li>
</ul>
<p>Thanks to Cabinet Office for enhanced data</p>
<ul>
<li>Cabinet Office has helpfully enhanced the data this month, providing sector (of the customer) and whether the supplier is a SME or not.  
We have incorporated both in the analysis now.  You will find an updated list of sectors and a breakdown of SME spend / non SME spend on the G-Cloud Spend page.</li>
</ul>

<p>Is government enhancing supplier diversity? It seems so.  The organisations that have engaged more than 10 suppliers via G-Cloud are:</p>
<ul>
<li>Cabinet Office - 57 suppliers</li>
<li>Home Office - 56 suppliers</li>
<li>MoJ - 37 suppliers</li>
<li>Defra - 21 suppliers</li>
<li>DoH - 14 suppliers</li>
<li>DfE - 13 suppliers</li>
<li>HMRC - 12 suppliers</li>
<li>Maritime & Coastguard Agency - 12 suppliers</li>
<li>BIS - 11 suppliers</li>
<li>FCO - 10 suppliers</li>
</ul>


<p>Spread of the framework</p>
<ul>
<li>23 new customers joined the framework this month (bringing the total to 437).</li> 
<li>21 new suppliers joined the framework this month (bringing the total to 300).</li> 
</ul>
<p>Forecasts</p>
<ul>
<li>Spend is building strongly.  The framework is adding 20 to 30 new buying organisations each month.</li>
<li>It is likely that that spend will regularly top £20m a month this quarter and be around £30m a month by year end.</li>
<li>We are holding our forecast for 2014 to £200m but if growth continues, will be revising this upwards in coming months.  £250m is looking achievable and £300m is within sight.  </li>
</ul>
