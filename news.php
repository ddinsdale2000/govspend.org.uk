<h1>G-Cloud News</h1>


<h2>In summary</h2>

<p><b>Review of May data</b></p>
<ul>
<li>The number of customers continues to grow - up 15  (bringing the total to 816).</li> <br>
<li>The number of suppliers is growing - up 15 (bringing the total to 566).</li> <br>

</ul>

<p><b>Overall framework stats</b></p> 

<ul>
<li>Total spend is via G-Cloud is £639m</li><br>
<li>Spend by year is £191m in 2015 (so far), £353m in 2014, £87m in 2013 and £6.6m in 2012.</li><br>
<li>Spend with Small and Medium Sized Enterprises (SMEs) continues at pace - £97m in 2015 so far, £173m in 2014 with £42m in 2013. </li> <br>
<li>After a what looks like a pre-election boost, monthly spend is stabilising around £40m a month (expect May figures to be revised upwards by about £10m when the next data set is released).</li>
</ul>

<p><b>Changes this month</b></p> 

<ul>
<li>The only frustrating part of running this service is that Cabinet Office tends to change the format of the data feed without notice.  This month the 
