<p>Spread of the framework</p>
<ul>
<li>The number of customers continues to grow strongly - up 43 last month and 18 this month (bringing the total to 491).</li> <br>
<li>The number of suppliers is growing, but more slowly than the customer base - up 12 last month and 10 this month (bringing the total to 322).</li> 
</ul>
<p>Forecasts</p>
<ul>
<li>We have revised our forecast for 2014 to £250m (up from £200m). </li><br>
<li>Spend is building strongly and the number of organisations buying from the framework is growing.</li><br>
<li>The trend figure for spend topped £20m a month for the first time in April.  We expect average monthly spend to increase to £30m a month by year end.</li><br>
</ul>


